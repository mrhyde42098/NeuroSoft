/* Helpers de filtrado */
export const filtrarRecursos = (recursos, { texto = "", categoria = "todos", nivel = "" } = {}) => {
  const q = (texto || "").trim().toLowerCase();
  return recursos.filter(r => {
    if (categoria !== "todos" && r.categoria !== categoria) return false;
    if (nivel && r.nivel !== nivel) return false;
    if (!q) return true;
    const blob = [r.titulo, r.autores, r.resumen, r.fuente, (r.tags || []).join(" ")]
      .join(" ").toLowerCase();
    return blob.includes(q);
  });
};
